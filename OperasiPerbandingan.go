package main

import "fmt"

func main() {

	// Operasi perbandingan untuk membandingkan sesuatu

	// Contoh Penggunaan Operasi Perbandingan :

	angka1 := 10
	angka2 := 10

	// apakah angka1 isi nya sama dengan angka2?
	result := angka1 == angka2
	 fmt.Println(result)
	 // apakah angka1 isi nya tidak sama dengan angka2?
	 result2 := angka1 != angka2
	 fmt.Println(result2)
	 // apakah angka1 isi nya lebih besar dari angka2?
	 result3 := angka1 > angka2
	 fmt.Println(result3)
	  // apakah angka1 isi nya lebih kecil dari angka2?
	 result4 := angka1 < angka2
	 fmt.Println(result4)
 // apakah angka1 isi nya lebih besar dari angka2?
	 result5 := angka1 >= angka2
	 fmt.Println(result5)
	  // apakah angka1 isi nya lebih kecil sama dengan dari angka2?
	 result6 := angka1 <= angka2
	 fmt.Println(result6)
}
