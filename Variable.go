package main

import "fmt"

func main() {

	// Variable merupakan tempat menyimpan data yang berisi identitas dari tipe data
	// variable digunakan untuk mengakses data
	// variable biasanya memiliki nama
	// pembuatan variable : var nama_var = "data" or nama_var := "data"
	// data variable tersebut bisa diisi berulang ulang sesuai kamu mau dengan keyword nama_var_yangDiulang = data_timpa;

	// contoh penggunaan variable :
	var name = "habib"
	var age = 20
	var isTrue = true

	fmt.Println(name)
	fmt.Println(age)
	fmt.Println(isTrue)

	// OR
	name2 := "habibr"
	age2 := 20
	isFalse := true

	fmt.Println(name2)
	fmt.Println(age2)
	fmt.Println(isFalse)


}
